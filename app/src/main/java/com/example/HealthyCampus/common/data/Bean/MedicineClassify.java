package com.example.HealthyCampus.common.data.Bean;

public class MedicineClassify {
    private String description;
    private int drugclass;
    private int id;
    private String keywords;
    private String name;
    private int seq;
    private String title;

    public String getDescription() {
        return description;
    }

    public int getDrugclass() {
        return drugclass;
    }

    public int getId() {
        return id;
    }

    public String getKeywords() {
        return keywords;
    }

    public String getName() {
        return name;
    }

    public int getSeq() {
        return seq;
    }

    public String getTitle() {
        return title;
    }


}
